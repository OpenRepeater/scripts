#!/bin/bash
################################################################################
# DEFINE SERIAL UART FUNCTIONS
################################################################################
function enable_uart () {
    if [ "$system_arch" == "armhf" ] || [ "$system_arch" == "arm64" ]; then
        echo "--------------------------------------------------------------"
        echo " Enable UART for serial console "
        echo "--------------------------------------------------------------"
		cat >> /boot/config.txt <<- DELIM
			#####################################################################
			# Enable uart serial console
			#####################################################################
			enable_uart=1
			DELIM
    fi
}
